            <p>Horizont hanteert twee betalingstermijnen:</p>
            <ul>
                <li>Bij boeking meer dan 6 weken voor vertrek volstaat een niet-restitueerbare aanbetaling van doorgaans 25% tot 50% van de Pakketreissom. Het restant dient uiterlijk 6 weken voor vertrek te zijn voldaan.</li>
                <li>Bij boeking binnen 6 weken voor vertrek betaalt de Hoofdboeker direct de volledige Reissom (100%).</li>

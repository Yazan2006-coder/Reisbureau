            <p>Na boeking is wijzigen niet altijd mogelijk en kan aanzienlijke kosten met zich meebrengen. Naast kosten van de Dienstverlener brengt D-reizen € 34,50 wijzigingskosten per keer in rekening. Wijzigingsverzoeken binnen 6 weken voor vertrek worden niet in behandeling genomen.</p>


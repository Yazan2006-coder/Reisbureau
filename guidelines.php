            <p>De Hoofdboeker dient tenminste 18 jaar oud te zijn, juridisch bevoegd te zijn om contractuele verbintenissen aan te gaan en de Website te gebruiken in overeenstemming met deze Algemene Voorwaarden. De Hoofdboeker is financieel aansprakelijk voor zijn/haar activiteiten op de Website.</p>


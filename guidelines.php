            <p>Indien de Hoofdboeker binnen 24 uur na boeking geen bevestiging heeft ontvangen, dient contact te worden opgenomen via info@horizont.nl. De boekingsbevestiging wordt niet later dan 10 dagen na de besteldatum verstrekt.</p>

            <h3>2.3.1 Annuleren</h3>
            <p>Bij annulering worden de annuleringskosten van de Dienstverlener in rekening gebracht, plus € 34,50 administratiekosten per annulering. Het totaal aan annuleringskosten bedraagt maximaal eenmaal de Reissom.</p>

